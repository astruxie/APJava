public class Pet {

		//variables
		private String name;
		private String animal;
		private int age;

		//set methods
		public void setName (String n) {
			name = n;
			}
		public void setAnimal(String ani){
			animal = ani;
			}
			public void setAge(int a){
			age = a;
			}
		//get methods
		public String getName(){
			return name;
			}
		public String getAnimal(){
			return animal;
			}
		public int getAge(){
			return age;
			}


}
